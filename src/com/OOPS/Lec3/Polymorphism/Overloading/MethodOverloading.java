package com.OOPS.Lec3.Polymorphism.Overloading;

public class MethodOverloading {
    //first type of polymorphism
    //methods with the same name but different Argument,return type,or order of arguments can be used called as Method overloading

    int sum (int a, int b){
        return a+b;
    }

    int sum(int a,int b,int c){
        return a+b+c;
    }


}


